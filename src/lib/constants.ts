/** Flat list of all location names (cities + universities) for validation */
export function getAllLocationNames(): string[] {
  const names: string[] = [];
  for (const group of LOCATIONS) {
    names.push(group.city);
    names.push(...group.universities);
  }
  return names;
}

/** Get the parent city for a location (returns the city itself if it's a city) */
export function getCityForLocation(location: string): string {
  for (const group of LOCATIONS) {
    if (group.city === location) return group.city;
    if (group.universities.includes(location)) return group.city;
  }
  return location;
}

